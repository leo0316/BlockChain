	  <p class="mb-5">Our team consists of four people, each of whom is an ordinary student in computer science department. During our life as a student,we found that it is very important to successfully choose the course you want. So we started to think about what kind of the course selection system is suitable, fair, and safe for students.</p>
<p class="mb-5">The course selection system is designed to enable student to select their desired courses. However, some popular courses are difficult to select via online course selection system. Most schools have their own rules for selection courses. There are two very common rules: one is to make decisions of course selections based on lottery drawing, and the other one is first select first win. However, both rules cannot exactly present the real desires of the courses for all students. Therefore, in this work, we adopt blockchain technology to make campus learning coin to implement a fair online course selection system based on Ethernet virtual machine (EVM) and Ethereum Request for Comments 20 (ERC20) Agreement. The leaning coins are issued dependent on Grade Point Average (GPA) and the number of selected courses. Afterwards, students may select courses by using their leaning coins like bidding system. They submit a tender by using learning coins for course selection. If a student is very eager to select a specific course, he can submit more coins for this course for bidding. Finally, the proposed course selection system based on learning coins provide a more harmonious for teaching and learning. In the future work, we consider adding more rules (e.g., the number of general education courses, and the priority of higher grade student), into smart contract to implement a fairer online course selection system. 


